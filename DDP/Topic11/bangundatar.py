# 1
def segitiga (a, t):
    luas = 1/2 * a * t
    keliling = a + a + a
    print("Luas Segi Tiga Adalah :",(luas))
    print("Keliling Segi Tiga Adalah :",(keliling))

# 2
def persegi (s):
    luas = s * s
    keliling = 4 * s
    print("Luas Persegi Adalah :", (luas))
    print("Keliling Persegi Adalah :", (keliling))

# 3
def persegipanjang (p, l):
    luas = p * l
    keliling = 2*p + 2*l
    print("Luas Persegi panjang Adalah :", (luas))
    print("Keliling Persegi Panjang Adalah :", (keliling))

